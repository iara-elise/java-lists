/* Escreva um algoritmo que imprima na tela do usuário os números inteiros de 1 até 100
(em ordem decrescente). */

public class Q02 {
    public static void main(String[] args) {

        for (int i = 100; i >= 1; i--) {
            System.out.println(i);
        }
    }
}

